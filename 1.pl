    издание(ID_изд, Издание, Тема, _),
    write("Тема  издания ",Издание, ": ", Тема), nl, fail. 
    
%Правило.Сколько денег потратили кентики
затраты_на_подписку(ID_клиента):-
    подписчик(ID_клиента, Имя, _, _),
    издание(_,_,_,Цена),
    подписка(_,_,_,Месяцы),
    Сумма = (Цена * Месяцы),
    write("Затраты ", Имя,": ", Сумма, " рублей"), nl.