%Правило.Кто подписан на издание
подписка_на_издание(ID_издания):-
    издание(ID_издания, Издание, _, _),
    подписчик(ID_подписчика, Имя, _,_),
    подписка(ID_издания, ID_подписчика, _,_),
    write(Издание, ": ", Имя), nl, fail.
    
%Правило.Тип издания
